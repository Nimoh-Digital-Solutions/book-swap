// Main
// ---------------------------------------------------------------------------

/**
 * Apply PWA additions.
 * @param {Object} [options]
 * @param {string} [options.rootDir]
 * @returns {Promise<{changed: boolean}>}
 */
export async function apply({ rootDir = getRootDir() } = {}) {
  if (hasPWA(rootDir)) {
    logWarn('PWA support already detected — no changes made.');
    return { changed: false };
  }

  logStep('Adding PWA support');

  const results = {
    pkg: addPwaDepsToPackageJson(rootDir),
    files: createPWAFiles(rootDir),
  };

  logInfo(`package.json: ${results.pkg ? '✓ updated' : '- no changes'}`);
  if (results.files > 0) logOk(`Created ${results.files} PWA file(s)`);

  console.log('\nNext steps:');
  console.log('  1. Run: yarn install');
  console.log('  2. Update vite.config.ts to import plugins from ./plugins');
  console.log('     import { pwaPlugin, htmlTransformPlugin } from \'./plugins\';');
  console.log('  3. Update index.html to add manifest link and PWA meta tags');
  console.log('  4. Update public/manifest.webmanifest with your app name/icons');
  console.log('  5. Add PWA icons to public/icons/ (192x192 and 512x512 PNG)');

  const changed = results.pkg || results.files > 0;
  return { changed };
}

/* ---------------- Direct Run ---------------- */

if (isDirectRun(import.meta.url)) {
  apply().catch(console.error);
}
