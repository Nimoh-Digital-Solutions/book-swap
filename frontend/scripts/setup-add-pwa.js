const PLUGINS_PWA_TS = `import { VitePWA } from 'vite-plugin-pwa';

/**
 * PWA Plugin Configuration
 *
 * Strategy:
 * - Production: enabled (generateSW) with precaching + runtime caching
 * - Development: opt-in via enableDev (prevents stale-cache confusion for daily dev)
 */
export function pwaPlugin({ isProd, enableDev }: { isProd: boolean; enableDev: boolean }) {
  return VitePWA({
    strategies: 'generateSW',
    registerType: 'autoUpdate',
    manifest: false,
    includeAssets: ['favicon.svg', 'icons/*.png'],
    workbox: {
      navigateFallback: '/index.html',
      ...(isProd && {
        globPatterns: ['**/*.{js,css,html,ico,png,svg,woff,woff2,webmanifest}'],
      }),
      runtimeCaching: [
        {
          urlPattern: ({ request }) => request.destination === 'image',
          handler: 'CacheFirst',
          options: {
            cacheName: 'images',
            expiration: { maxEntries: 100, maxAgeSeconds: 60 * 60 * 24 * 30 },
          },
        },
        {
          urlPattern: /^https:\\/\\/fonts\\.googleapis\\.com\\/.*/i,
          handler: 'CacheFirst',
          options: {
            cacheName: 'google-fonts-cache',
            expiration: { maxEntries: 10, maxAgeSeconds: 60 * 60 * 24 * 365 },
          },
        },
      ],
      cleanupOutdatedCaches: true,
      skipWaiting: true,
      clientsClaim: true,
    },
    devOptions: {
      enabled: enableDev,
      type: 'module',
    },
  });
}
`;

const PLUGINS_HTML_TRANSFORM_TS = `import type { Plugin } from 'vite';

/**
 * Conditionally inject PWA manifest link based on environment
 */
export function htmlTransformPlugin(enablePwa: boolean): Plugin {
  return {
    name: 'html-transform',
    transformIndexHtml(html) {
      if (!enablePwa) {
        return html
          .replace(/<link rel="manifest"[^>]*>/g, '')
          .replace(
            /<!-- PWA capabilities -->[\\s\\S]*?<meta name="msapplication-TileColor"[^>]*>/g,
            ''
          );
      }
      return html;
    },
  };
}
`;

const SW_PWA_TS = `/**
 * PWA Registration and Update Management
 *
 * This file handles PWA registration and update notifications.
 * The service worker is auto-generated by vite-plugin-pwa.
 */
import { registerSW } from 'virtual:pwa-register';

let updateSW: (reloadPage?: boolean) => Promise<void>;
let showUpdatePrompt: (() => void) | null = null;

async function unregisterServiceWorkers() {
  if ('serviceWorker' in navigator) {
    const registrations = await navigator.serviceWorker.getRegistrations();
    for (const registration of registrations) {
      await registration.unregister();
    }
  }
}

export function initPWA() {
  if (import.meta.env.DEV && import.meta.env.VITE_PWA !== 'true') {
    unregisterServiceWorkers().catch(err =>
      console.warn('Failed to unregister service workers:', err)
    );
    return;
  }

  updateSW = registerSW({
    onNeedRefresh() {
      if (showUpdatePrompt) {
        showUpdatePrompt();
      } else {
        updateSW(true);
      }
    },
    onOfflineReady() {
      console.info('[PWA] App is ready for offline use.');
    },
  });
}

export function setUpdatePromptHandler(handler: () => void) {
  showUpdatePrompt = handler;
}

export function triggerUpdate() {
  if (updateSW) updateSW(true);
}
`;

