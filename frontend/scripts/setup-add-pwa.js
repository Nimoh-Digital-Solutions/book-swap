/**
 * Check if PWA is already present.
 * @param {string} rootDir
 * @returns {boolean}
 */
function hasPWA(rootDir) {
  const pkgPath = path.join(rootDir, 'package.json');
  if (exists(pkgPath)) {
    const pkg = readJson(pkgPath);
    if (pkg.devDependencies?.['vite-plugin-pwa'] || pkg.dependencies?.['vite-plugin-pwa']) return true;
  }
  if (exists(path.join(rootDir, 'plugins/pwa.ts'))) return true;
  if (exists(path.join(rootDir, 'public/manifest.webmanifest'))) return true;
  return false;
}

/**
 * Add vite-plugin-pwa to devDependencies.
 * @param {string} rootDir
 * @returns {boolean} True if changes were made
 */
function addPwaDepsToPackageJson(rootDir) {
  const pkgPath = path.join(rootDir, 'package.json');
  if (!exists(pkgPath)) return false;

  const pkg = readJson(pkgPath);
  let changed = false;

  if (!pkg.devDependencies) pkg.devDependencies = {};
  if (!pkg.devDependencies['vite-plugin-pwa']) {
    pkg.devDependencies['vite-plugin-pwa'] = '^1.0.3';
    changed = true;
  }

  if (changed) writeJson(pkgPath, pkg);
  return changed;
}

/**
 * Create PWA files.
 * @param {string} rootDir
 * @returns {number} Number of files created
 */
function createPWAFiles(rootDir) {
  let created = 0;

  const files = [
    ['plugins/pwa.ts', PLUGINS_PWA_TS],
    ['plugins/html-transform.ts', PLUGINS_HTML_TRANSFORM_TS],
    ['plugins/index.ts', PLUGINS_INDEX_TS],
    ['src/sw/pwa.ts', SW_PWA_TS],
    ['src/types/pwa.ts', TYPES_PWA_TS],
    ['src/utils/_pwa-state.ts', PWA_STATE_TS],
    ['public/manifest.webmanifest', MANIFEST_WEBMANIFEST],
  ];

  for (const [relPath, content] of files) {
    const filePath = path.join(rootDir, relPath);
    const dir = path.dirname(filePath);

    if (!exists(dir)) fs.mkdirSync(dir, { recursive: true });

    if (!exists(filePath)) {
      writeText(filePath, content);
      logInfo(`${relPath}: ✓ created`);
      created++;
    } else {
      logInfo(`${relPath}: - already exists, skipped`);
    }
  }

  return created;
}

